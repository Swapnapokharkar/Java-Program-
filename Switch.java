package test;

public class Switch {

	public static void main(String[] args) {
		int num=2;
		switch(num+2)
		{
		case 1:
		System.out.println("case1");
		case2:
		System.out.println("case2");
		case3:
		System.out.println("case3");
		default:
		System.out.println("default");
		}
	}	
}
